class RecyclerItemInvChangeTypeMAdapter() :
    RecyclerView.Adapter<RecyclerItemInvChangeTypeMAdapter.ViewHolder>() {
    private val itemData: ShowInvChangeTypeM =Gson().fromJson(cookie_data.response_data, ShowInvChangeTypeM::class.java)
    private var data: ArrayList<InvChangeTypeM> =itemData.data

    override fun onCreateViewHolder(parent: ViewGroup, viewType: Int): RecyclerItemInvChangeTypeMAdapter.ViewHolder {
        val v= LayoutInflater.from(parent.context).inflate(R.layout.recycler_item_inv_change_type_m,parent,false)
        return ViewHolder(v)

    }

